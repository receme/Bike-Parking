    @Override
    public void onMapReady(GoogleMap googleMap) {

        CameraUpdate cameraUpdate = CameraUpdateFactory.newCameraPosition(CameraPosition.fromLatLngZoom(new LatLng(23,90),10));
        googleMap.moveCamera(cameraUpdate);
    }

